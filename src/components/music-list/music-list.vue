<template>
  <div class="music-list">
    <div class="header">
      <div class="back"
           @click="back">
        <i class="fa fa-angle-left"></i>
      </div>
    </div>
    <div class="bg-image"
         :style="bgStyle"
         ref="bgImage">
      <div class="text"
           v-html="title"></div>
      <div class="play-wrapper">
        <div class="play"
             v-show="songs.length > 0"
             ref="playBtn">
          <i class="iconfont icon-random-play"></i>
          <span class="play-text">随机播放全部</span>
        </div>
      </div>
      <div class="filter"
           ref="filter"></div>
    </div>
    <div class="bg-layer"
         ref="bgLayer">
    </div>
    <scroll :data="songs"
            :probeType="probeType"
            :listenScroll="listenScroll"
            @scroll="scroll"
            class="list"
            ref="list">
      <div class="song-list-wrapper">
        <song-list :songs="songs"></song-list>
      </div>
    </scroll>
    <div class="loading-container"
         v-show="!songs.length">
      <loading></loading>
    </div>
  </div>
</template>

<script>
import Scroll from 'base/scroll/scroll'
import SongList from 'base/song-list/song-list'
import Loading from 'base/loading/loading'
import { prefixStyle } from 'common/js/dom'

const OFFSET_TOP = 20
const RESERVED_HEIGHT = 40
const transform = prefixStyle('transform')
const backdrop = prefixStyle('backdrop-filter')
export default {
  props: {
    songs: {
      type: Array,
      default () {
        return []
      }
    },
    title: {
      type: String,
      default: ''
    },
    bgImage: {
      type: String,
      default: ''
    }
  },
  data () {
    return {
      scrollY: 0
    }
  },
  computed: {
    // 处理背景
    bgStyle () {
      return `background-image: url(${this.bgImage})`
    }
  },
  created () {
    this.probeType = 3
    this.listenScroll = true
  },
  mounted () {
    this.imageHeight = this.$refs.bgImage.clientHeight
    // 最小滚动距离
    this.minTranslateY = -this.imageHeight + RESERVED_HEIGHT + OFFSET_TOP

    this.$refs.list.$el.style.top = this.imageHeight - OFFSET_TOP + 'px'
    this.$refs.bgLayer.style.top = `-${OFFSET_TOP + 41}px`
  },
  methods: {
    scroll (pos) {
      this.scrollY = pos.y
    },
    back () {
      this.$router.back()
    }
  },
  watch: {
    scrollY (newY) {
      let translateY = Math.max(this.minTranslateY, newY)
      let zIndex = 0
      let scale = 1
      let blur = 0

      this.$refs.bgLayer.style[transform] = `translate3d(0,${translateY}px,0)`
      // 下拉图片放大
      const percent = Math.abs(newY / this.imageHeight)
      if (newY > 0) {
        scale = 1 + percent
        zIndex = 10
      } else {
        blur = Math.min(20 * percent, 20)
      }
      // backdrop 针对ios设置模糊渐变效果
      this.$refs.filter.style[backdrop] = `blur(${blur}px)`

      // 向上滚动,固定header
      if (newY < this.minTranslateY) {
        zIndex = 10
        this.$refs.bgImage.style.paddingTop = 0
        this.$refs.bgImage.style.height = `${RESERVED_HEIGHT}px`
        this.$refs.playBtn.style.display = 'none'
      } else {
        zIndex = 0
        this.$refs.bgImage.style.paddingTop = '70%'
        this.$refs.bgImage.style.height = 0
        this.$refs.playBtn.style.display = ''
      }
      this.$refs.bgImage.style.zIndex = zIndex
      // 设置下拉放大图片的比例
      this.$refs.bgImage.style[transform] = `scale(${scale})`
    }
  },
  components: {
    SongList,
    Scroll,
    Loading
  }
}
</script>

